Icon Library Documentation

This component provides a simple way to use icons throughout the application
without writing complex SVG code.

Usage:
<x-icon name="icon-name" class="w-5 h-5" />

Available Icons:
- dashboard: Dashboard/home icon
- users: Users/people icon
- gold: Gold/currency icon
- electronics: Electronics/device icon
- faq: Question mark/FAQ icon
- plus: Plus/add icon
- search: Search/magnifying glass icon
- edit: Edit/pencil icon
- view: Eye/view icon
- delete: Delete/trash icon
- export: Export/download icon
- pdf: PDF document icon
- excel: Excel document icon
- filter: Filter/funnel icon
- settings: Settings/gear icon
- check: Checkmark/success icon
- x: X/close icon
- chevron-down: Down arrow
- chevron-up: Up arrow
- chevron-left: Left arrow
- chevron-right: Right arrow
- menu: Hamburger menu icon
- close: Close/X icon
- warning: Warning triangle icon
- info: Information circle icon
- success: Success checkmark icon
- error: Error/X icon
- default: Default fallback icon

Examples:
<x-icon name="users" class="w-6 h-6" />
<x-icon name="electronics" class="w-4 h-4 text-blue-500" />
<x-icon name="plus" class="w-5 h-5 text-green-600" />

For sidebar icons, use the sidebar-icon component:
<x-sidebar-icon name="dashboard" />
<x-sidebar-icon name="users" />
--}}
